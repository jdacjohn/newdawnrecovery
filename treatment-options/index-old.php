<div id="slidingDiv3" style="display: none;"><p>Intensive outpatient treatment clients participate in our program 3 hours a day, Monday through Saturday. The program is designed for those who need more structure and support than a typical outpatient program, or for those who may be transitioning from our <a href="#PHP">day treatment program</a> or our <a href="#RTP">residential treatment program</a>. The intensive outpatient program provides comprehensive treatment while allowing you to retain autonomy in your normal day-to-day activities.</p>


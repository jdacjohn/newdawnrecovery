<h2> Day Treatment or PHP (Partial Hospitalization)</h2>


<a href="#" class="show_hide" rel="#slidingDiv2"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv2" style="display: none;"><p>Day Treatment consists of 6-8 hours of treatment per day and available five days a week. Individual therapy and family counseling sessions are included as needed. The program is designed for those who need additional structure, support, and supervision in order to sustain or stabilize their recovery process or for individuals who may be transitioning from inpatient or residential treatment. PHP can prevent hospitalization for some. Most individuals step down to our <a href="#IOP">intensive outpatient program</a> after completing our day treatment program.</p>

</div></div>


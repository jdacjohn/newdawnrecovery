<h2>Residential Treatment Program</h2>
Our residential programs provide supportive, structured therapeutic environments in which to overcome eating disorders, chemical dependency and other related mental health symptoms.  &nbsp;

<div id="slidingDiv" style="display: none;">Our community offers a safe environment with a clear set of guidelines and expectations where you will have the opportunity to learn and practice positive coping skills. Traditional individual, family, and group psychotherapy, supervised meals/snacks (eating disorder program), nutritional counseling (eating disorder program), and medical and psychiatric monitoring are integral to the treatment, as well as more creative and holistic 
modes of healing such as expressive arts, movement therapy and meditation.

<h2>Detox</h2>
Few Inpatient programs can provide Detox services in a setting that is separate and distinct from their Residential Program because of the expense involved with more real-estate and additional staff.

<div id="slidingDiv8" style="display: none;">
<p> However, because our goal is to provide the maximum level of privacy and confidentiality, our Detox houses are separate from our Residential Treatment facilities, yet only by a block so that all the services and support provided by the Residential Programs are also available to the Detox patients.</p>
<p>
Our program has its own dedicated 24-hour staff consisting of a nurse, a certified addiction counselor and medical assistants. It is designed to provide a safe, comfortable environment that supports the needs of those requiring sub-acute medical care. Typically, the detoxification period ranges from 7-14 days depending on each individual's physical and psychological condition and the rate at which they progress through the withdrawal period.
</p>


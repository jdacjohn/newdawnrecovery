<h2> Day Treatment or PHP (Partial Hospitalization)</h2>
Day Treatment consists of 6-8 hours of treatment per day and is available five to six days per week. &nbsp;

<div id="slidingDiv2" style="display: none;">The program is designed for those who need additional structure, support, and supervision in order to sustain or stabilize their recovery process or for individuals who may be transitioning from inpatient or residential treatment. PHP can prevent hospitalization for some. Most individuals step down to our intensive outpatient program after completing our day treatment program.



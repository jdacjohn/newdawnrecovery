<h2>Adolescent Chemical Dependency Program</h2>
New Dawn's Adolescent Intensive Outpatient and Partial Hospitalization program is the only outpatient program in the Sacramento area that offers structured drug and alcohol abuse treatment and prevention five nights per week.

<div id="slidingDiv9" style="display: none;"><p>Our treatment philosophy is based on the biopsychosocial model of addiction, which posits that a mix of various biological, psychological, and social factors can play a role in contributing to one's mental and behavioral functioning. Treatment involves the development of an individualized treatment plan based on the unique therapeutic needs of each person. 
<br /><br />
Services include individual and group counseling, weekly parent groups, family therapy, recreational therapy and weekly aftercare groups. The curriculum focuses on the issues that most often contribute to a teenager's drug or alcohol abuse. Some of the topics that are covered include the following: 
</p>
<ul>
<li>emotions and drug use</li>
<li>cravings and urges</li>
<li>healthy living</li>
<li>managing life's difficulties</li>
<li>coping skill development</li>
<li>building self-esteem</li>
<li>Changing Distorted Thinking</li>
<li>developing healthy boundaries</li>
<li>relapse prevention</li>
</ul>
<p>Our counselors are trained and certified in mental health and substance abuse counseling. We bring a wealth of knowledge and experience with a caring attitude to the difficulties that families faces in dealing with mental health and/or substance abuse issues. </p>
 
<p>In addition to accepting most insurances, we also receive up to 100% funding for clients who do not have an insurance benefit under the C.A.R.E. (California Access to Recovery Efforts) program, so please call us if you would like your child to receive a free, confidential assessment.</p>
 


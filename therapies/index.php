<img src="../images/Therapies_empower.jpg" align="right" class="therapies-img"/>

<h2>Empowerment</h2>
Empowerment is the process which enables us to fully access our internal power to overcome our external circumstances.  &nbsp;

<div id="slidingDiv11" style="display: none;">Empowered individuals have the ability to consider a range of options, make informed decisions about their personal situation, and possess the positive thinking to create change.

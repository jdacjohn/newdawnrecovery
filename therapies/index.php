<h1>Treatment Approach</h1>
<p>Because any recovery process needs to be collaborative and person-centered to be effective, we offer a whole person approach that includes a comprehensive array of therapies and experiences to aid in the improvement of mind, body and spirit. </p>

<h1>Core Therapies</h1>
<a name="cbt"></a>
<div class="ExpandingDiv">

<h2>Cognitive Behavioral Therapy (CBT)</h2><img src="../images/Therapies_cbt.jpg"  width="150" height="100" align="right" class="therapies-img"/>
Cognitive Behavioral Therapy is an evidence-based treatment approach that explores a person's thoughts, feelings, and behavior in order to identify how unhelpful or faulty thinking patterns contribute to unhealthy behaviors and increased negative emotional experiences.  &nbsp;

<div id="slidingDiv4" style="display: none;">
 The treatment focuses on identifying an individual's core beliefs and distorted thinking patterns and attempts to restructure one's thoughts  in order to promote positive changes in emotional state and behavior. 
</div>
<a href="#" class="show_hide" rel="#slidingDiv4" id="cbt_BTN">Learn More</a>
</div>

<a name="dbt"></a>
<div class="ExpandingDiv">
<img src="../images/Therapies_DBT.jpg"  width="150" height="100" align="right" class="therapies-img"/>

<h2>Dialectical Behavioral Therapy (DBT)</h2>
Dialectical Behavioral Therapy is an evidence-based therapeutic approach that focuses on both problem-solving and skill building in order to assist individual's in identifying how particular behaviors cause problems, the types of situations, thoughts, feelings, and behaviors that lead to these problems, and what new behaviors or skills one may be used to prevent problem behaviors from occurring.  &nbsp;

<div id="slidingDivDBT" style="display: none;">DBT focuses on decreasing misery and distress with the use of four core skills: Mindfulness, Distress Tolerance, Emotional Regulation, and Interpersonal Effectiveness.
</div>
<a href="#" class="show_hide" rel="#slidingDivDBT" id="cbt_BTN">Learn More</a>
</div>

<a name="cbt"></a>
<div class="ExpandingDiv" style="border:none!important;">


<h2>Acceptance and Commitment Therapy (ACT)</h2><img src="../images/Therapies_ACT.jpg"  width="150" height="100" align="right" class="therapies-img"/>
Acceptance and Commitment Therapy is an evidence-based therapeutic  approach that focuses on the use of acceptance and mindful awareness to assist individual's in learning to relate differently to emotional pain. &nbsp;

<div id="slidingDivACT" style="display: none;"> The core premise of ACT is that human beings avoid negative emotional experiences and this avoidance causes further suffering because it decreases one's ability to engage in a life consistent with his or her personal values.  Thus, ACT aims to clarify personal values and take action based on those values so that an individual is able to obtain a meaningful life.
</div>
<a href="#" class="show_hide" rel="#slidingDivACT" id="cbt_BTN">Learn More</a>
</div>
<br />

<h1>Experiential Therapies</h1>
<p>Our goal with every participant is to determine a combination of modalities that will work best in facilitating their recovery and resolving to create healthy thoughts, feelings and behaviors. Therapy groups tailored to our <a href="http://www.newdawntreatmentcenters.com/therapies/eating-disorder-therapies/">Eating Disorder</a> and <a href="http://www.newdawntreatmentcenters.com/therapies/chemical-dependency-therapies/">Chemical Dependency</a> participants needs encourage personal growth and enrichment.
</p>
<br />
<a name="family"></a>
<div class="ExpandingDiv">
<img src="../images/Therapies_Family-Group.jpg"  width="150" height="100" align="right" class="therapies-img"/>
<h2>Family Group</h2>
Our family groups are an integral part of the therapeutic process at New Dawn Treatment Centers. &nbsp;

<div id="slidingDiv" style="display: none;"><p>We understand the effects of addiction and eating disorders are wide reaching, especially in families. When family members are willing and engaged participants in the recovery process, there is potential to bring healing not only to program participant, but also to all family members. Our weekly family groups are a mix of education, process group, and experiential activities.
</p>
<p>Education is a cornerstone of our work at New Dawn. Arming our participants and families with knowledge about the disease and treatment is vital to our treatment success. The process portion of family group is an important piece because it allows family members to share with others their experiences of living with the recovering person. Through process, program participants normalize one another's feelings in an empathetic and safe space. Participants also provide feedback for one another and coping tools. Experiential activities create in-the-moment experiences that facilitate communication and emotional healing.</p>
</div>
<a href="#" class="show_hide" rel="#slidingDiv" id="family_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<img src="../images/Therapies_Process-Group.jpg"  width="150" height="100" align="right" class="therapies-img"/>

<h2>Process Group</h2>
The process group creates a space for program participants to share where they are at in the recovery process, and to address the issues with which they are currently struggling. &nbsp;

<div id="slidingDiv2" style="display: none;">While facilitated by a therapist, the goal of the process group is to allow participants to listen to one another and provide supportive feedback in a group setting. 
</div>
<a href="#" class="show_hide" rel="#slidingDiv2">Learn More</a>
</div>





<a name="arts"></a>
<div class="ExpandingDiv">
<img src="../images/Therapies_expressive-arts.jpg" align="right" class="therapies-img"/>

<h2>Expressive Arts</h2>
While traditional therapies and medicine do wonders in healing the mind and the body, our soul and spirit experience recovery on different levels of awareness. &nbsp;

<div id="slidingDiv7" style="display: none;">Inherently ethereal, art therapies allow program participants to explore their recovery through the visual arts, movement, drama, music, writing and other creative processes to better understand themselves, their addiction, and the fulfilling life waiting for them in recovery. These creative forms of self-expression are inner resources for healing and clarity.
</div>
<a href="#" class="show_hide" rel="#slidingDiv7" id="arts_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<img src="../images/Therapies_selfAcceptance.jpg" align="right" class="therapies-img"/>
<h2>Self-Acceptance</h2>
In order for positive change to occur, self-acceptance is critical.  &nbsp;

<div id="slidingDiv3" style="display: none;">How we relate to others and our environment is dependent on how we relate to ourselves. Developing self-compassion ends the blame and critique that interferes with an individual's happiness with one's self. 
</div>
<a href="#" class="show_hide" rel="#slidingDiv3">Learn More</a>
</div>





<a name="equine"></a>
<div class="ExpandingDiv">
<img src="../images/Therapies_equine.jpg" align="right" class="therapies-img" width="150" height="100"/>

<h2>Equine Program</h2>
Equine therapy incorporates a therapeutic relationship with horses to generate emotional growth, and improved communication. &nbsp;

<div id="slidingDiv9" style="display: none;">Equine therapy encourages self-awareness in program participants. Horses often emulate the behaviors and emotions of the participants through their body language fostering growth in non-verbal communication. The relationship with their equine partner can model the empathy and patience necessary to create positive, healthy relationships with their two-legged peers.
</div>
<a href="#" class="show_hide" rel="#slidingDiv9" id="equine_BTN">Learn More</a>
</div>






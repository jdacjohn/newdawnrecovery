<h2>Family Group</h2>



<a href="#" class="show_hide" rel="#slidingDiv"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv" style="display: none;"><p>Our family groups are an integral part of the therapeutic process at New Dawn Treatment Centers.  We understand that addiction does not happen in a vacuum, and its effects are wide reaching, especially in families. When family members are willing and engaged participants in the recovery process, there is potential to bring healing not only to program participant, but also to all family members. Our weekly family groups are a mix of chemical dependency education, process group, and experiential activities.  </p>

<p>Chemical dependency education is a cornerstone of our work at New Dawn. Arming our participants and families with knowledge about the disease and treatment of addiction is vital to our treatment success. The process portion of family group is an important piece because it allows family members to share with others their experiences of living with the addicted person. Through process, program participants normalize one another's feelings in an empathetic and safe space. Participants also provide feedback for one another and coping tools.  Experiential activities create in-the-moment experiences that facilitate communication and emotional healing. </p>
</div></div>

<div class="ExpandingDiv">

<h2>Process Group</h2>
<a href="#" class="show_hide" rel="#slidingDiv2"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv2" style="display: none;"><p>
The process group creates a space for program participants to share where they are at in the recovery process, and to address the issues with which they are currently struggling. While facilitated by a therapist, the goal of the process group is to allow participants to listen to one another and provide supportive feedback in a group setting. 

</p></div></div>


<div class="ExpandingDiv">

<h2>CBT, DBT &amp; ACT</h2>
<a href="#" class="show_hide" rel="#slidingDiv4"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv4" style="display: none;"><p>
Through cognitive behavioral interventions we address the distorted thinking and unworkable behavior patterns that permeate addictive processes. Through use of third wave cognitive behavioral  theories such as Dialectical Behavior Therapy and Acceptance &amp; Commitment Therapy, we teach our clients how to live in the present (mindfulness), cope with painful thoughts and emotions (distress tolerance and cognitive defusion), and take actions to create vision for their lives and live out their personal values.
</p></div></div>



<div class="ExpandingDiv">

<h2>Supervised Meals</h2>
<a href="#" class="show_hide" rel="#slidingDiv6"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv6" style="display: none;"><p>
We understand that your meal group experience is a critical part of the eating disorder recovery process. We also understand that it is common to fear the meal/snack component of the program. Similar to process group, we explore coping skills that ease any distressing thoughts and feelings that may emerge around eating. We help you overcome fear of any and all foods while providing support in taking food risks and increasing your comfort with eating and sharing a meal experience.
</p></div></div>

<div class="ExpandingDiv">

<h2>Expressive Arts</h2>
<a href="#" class="show_hide" rel="#slidingDiv7"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv7" style="display: none;"><p>
	While traditional therapies and medicine do wonders in healing the mind and the body, our soul and spirit experience recovery on  different levels of awareness.  Inherently ethereal, art therapies allow program participants to explore their recovery through the visual arts, movement, drama, music, writing and other creative processes to better understand themselves, their addiction, and the fulfilling life waiting for them in recovery.  These creative forms of self-expression are inner resources for healing and clarity.

</p></div></div>


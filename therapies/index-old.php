<div id="slidingDiv11" style="display: none;"><p>
Empowerment is the process which enables us to fully access our internal power to overcome our external circumstances. Empowered individuals have the ability to consider a range of options, make informed decisions about their personal situation, and possess the positive thinking to create change.
</p></div></div>

<div class="ExpandingDiv">

<h2>Holistic Nutrition</h2>
<a href="#" class="show_hide" rel="#slidingDiv12"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv12" style="display: none;"><p>
The quality of our food can influence our moods, thoughts and actions. Food not only provides the energy needed to function throughout our day but it is responsible for restoring our bodies' daily wear and tear. Incorporating natural and organic foods can increase the nutrients that are essential to produce the natural chemicals that keep our bodies in balance. Using a non-dieting, holistic approach based on intuitive eating, our Registered Dieticians help restore eating behaviors to ensure a safe and healthy normalization of body weight.
</p></div></div>

<div class="ExpandingDiv">

<h2>Intuitive Eating</h2>
<a href="#" class="show_hide" rel="#slidingDiv13"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv13" style="display: none;"><p>
	Unlike chemical addiction, abstinence from food is not a viable option. The goal in developing intuitive eating habits is make peace with food and re-engage the inner signals for hunger and fullness. Your worth as a person is constant and not dependent on your choice to eat a carrot stick over a sandwich. It is important to acknowledge what your body needs and wants for it to do its job. Mindfulness techniques help our program participants make the mind/body connection and promote a healthy relationship with food.
</p></div></div>

<div class="ExpandingDiv">


<h2>Equine Program</h2>
<a href="#" class="show_hide" rel="#slidingDiv9"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv9" style="display: none;"><p>
	Equine therapy incorporates a therapeutic relationship with horses to generate emotional growth, and improved communication. Equine therapy encourages self-awareness in program participants. Horses often emulate the behaviors and emotions of the participants through their body language fostering growth in non-verbal communication. The relationship with their equine partner can model the empathy and patience necessary to create positive, healthy relationships with their two-legged peers.
</p></div></div>


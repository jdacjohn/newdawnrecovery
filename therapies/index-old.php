<div id="slidingDiv14" style="display: none;"><p>
Fresh organic ingredients and customized meal plans are designed by our Registered Dietitians and chef prepared to guarantee a positive, healthy eating experience. Our Residential programs provide 4 week menu cycle that incorporate a wide variety of dishes to expand the palate. Cooking classes teach program participants to make their favorite recipes at home.
</p></div></div>


<p>Dr. Nipomnick has extensive training in the eating disorder field, as well as crisis intervention, and work with patients who have co-morbid medical and psychiatric conditions. Her research has focused on psychological control and recovery from anorexia nervosa along with body image and behavioral health interventions and outcomes. She is a member of the Academy for Eating Disorders and the American Psychological Association.</p>

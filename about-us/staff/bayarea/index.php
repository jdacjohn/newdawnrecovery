<div class="staffTitles">
<strong>	Licensed Clinical Psychologist, PSY24956 <br/>
	<em>Clinical Director, PHP/IOP Eating Disorder Sausalito</em></strong>
</div>  

<div id="slidingDiv" style="display: none;">

<p>Summer Nipomnick, Ph.D. is a licensed clinical psychologist who has been involved in the treatment of eating disorders for over a decade. She returns to New Dawn Treatment Centers after completing a postdoctoral fellowship at the University of Texas MD Anderson Cancer Center where she was actively engaged in the Body Image Therapy and Research Program. Dr. Nipomnick earned a Master’s degree in Counseling Psychology with an emphasis in marriage and family therapy from John F. Kennedy University. She completed her pre-doctoral internship at the University of California Davis Medical Center and received a Ph.D. in Clinical Psychology from Fielding Graduate University.

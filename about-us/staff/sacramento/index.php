<div class="ExpandingDiv">

<h3 class="staff">Abby Lockett, MA, RAS</h3>
<div class="staffTitles">
   <strong><em>Residential Counselor,</em> Orangevale Chemical Dependency</strong><br/>   
</div>

<div id="slidingDiv5" style="display: none;">
<p>Abby earned her Master's degree in Human Development Counseling from Saint Louis University in Missouri. Before starting her work at New Dawn Treatment Centers, Abby worked for Bridgeway Behavioral Health, a non-profit residential treatment center in St. Charles, Missouri. There she coordinated the Children's Program and provided family therapy services and individual counseling to men, women, children and adolescents.</p>
<p>Abby sees the "dis-ease" of addiction as an opportunity to transform the self from the inside out. Abby believes that seeing the light in addiction begins by creating a new view of who you are, and gaining a new understanding of the mind, body, and spirit. Creating light in one's life around areas of addiction requires new thoughts, behaviors, and actions that re-wire the brain and re-new the individual. Abby designs groups on developmental tasks in recovery that provide insight into the very humanness of our addictions. Her groups provide tools for creating a life of freedom, peace, and true self-expression. Supporting clients as they transform negative core beliefs and recover a sense of their true selves is core to the work she does at New Dawn.</p>
<p>Abby began as an adolescent therapist for New Dawn. She currently provides psycho-educational groups, individual and couples therapy, and contributes to the ongoing development of New Dawn treatment curriculum. Her interests include healing from trauma, shame, and unresolved grief. She is lead therapist on implementing and facilitating the Connections Curriculum at New Dawn, which is an evidenced-based Psycho-educational Shame Resilience Program. Abby's theoretical orientation includes CBT and ACT (Acceptance & Commitment Therapy). She is currently studying the work of Richard Schwartz and the Internal Family Systems model for the healing of trauma, and Dr. Brene Brown's research on vulnerability and shame.</p>


<h3 class="staff">June Ruse, Psy.D.</h3>
<div class="staffTitles">
	
   <strong>	Licensed Clinical Psychologist, PSY18472<br/>
	<em>Clinical Director, Chemical Dependency Services</em></strong><br/>   
</div>

<div id="slidingDiv11" style="display: none;">

<p>Dr. June May Ruse joins New Dawn Treatment Centers with over 30 years of experience in the treatment of Substance Dependence and Recovery. Throughout her career, Dr Ruse, has served as a therapist, clinical supervisor, administrator, and consultant for both hospital and non-hospital based inpatient and outpatient programs. Her ability to understand and work within systems serves as an agent to facilitate growth and development. The breadth and depth of her career is a reflection of her commitment to the reduction of harm and her understanding of the maturation process throughout recovery. </p>

<p>Dr. Ruse completed her undergraduate degree in Social Work at the University of Toledo in Ohio. She obtained her Master's Degree in Counseling Psychology from Siena Heights University in Adrian, Michigan. Her master's thesis focused on how the scope of women's needs in recovery often goes beyond what 12-Step based programs offer for women.  
She earned her Doctorate in Clinical Psychology from the Adler School of Professional Psychology in Chicago Illinois and completed her Post Doctoral residency with the University of California, San Francisco, Faculty and Staff Assistance Program and Kaiser Permanente Medical Center, South San Francisco, Chemical Dependency Services. She became a licensed Clinical Psychologist in the state of California in 2002.</p>
<p>Before her relocation to Placer County, she made her home in Fort Bragg, California. Dr. Ruse served the Mendocino Coast Clinics, Inc, in the role of Behavioral Medicine Specialist. In this role, she co-created the Suboxone Program with the Medical Director and helped manage the Chronic Pain Program with her colleague. She also served as an adjunct professor for The College of the Redwoods at Mendocino where she taught Social Psychology, Abnormal Psychology, and The Psychology of Personal Growth.
</p>


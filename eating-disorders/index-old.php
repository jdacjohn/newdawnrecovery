<h2>Anorexia Nervosa </h2>



<a href="#" class="show_hide" rel="#slidingDiv"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv" style="display: none;"><p>Anorexia nervosa is an eating disorder characterized by weight obsession, self-starvation, and distortion of body image. People with this psychiatric disorder strive to maintain a weight far below normal for their height and age commonly, but not exclusively, by limiting food, excessively exercising, or fasting. For the anorexic, the pursuit of being thin has transformed into an obsession and a way to cope with other emotional problems to have a sense of control or self-worth.
</p></div></div>

<div class="ExpandingDiv">

<h2>Bulimia Nervosa</h2>
<a href="#" class="show_hide" rel="#slidingDiv2"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv2" style="display: none;"><p>
Bulimia nervosa is an eating disorder characterized by habitual stints of excessive food intake, known as "binge-eating," by a purging of calories through means of self-induced vomiting, laxative abuse, periods of fasting, or excessive exercise as methods of weight control. These episodes are typically done in secret and are associated with shame since much of the bulimic's self worth originates in body shape and size. Like the anorexic, these behaviors can become addictive as they strive for their "ideal" body proportions.

</p></div></div>

<div class="ExpandingDiv">

<h2>Binge-Eating Disorder</h2>
<a href="#" class="show_hide" rel="#slidingDiv3"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv3" style="display: none;"><p>
Binge-eating disorder, like bulimia, is characterized by compulsively in taking large quantities of food at a single sitting, however, without purging away the calories afterwards. Over-eating becomes a habit and a coping mechanism for overcoming stress, depression, and other negative feelings. This emotional eating becomes an addiction and leads to very serious medical conditions linked with obesity such as high blood pressure and cholesterol levels.

</p></div></div>


<div class="ExpandingDiv">

<h2>Diabulimia </h2>
<a href="#" class="show_hide" rel="#slidingDiv4"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv4" style="display: none;"><p>
This eating disorder classification is characterized by people with Type 1 diabetes who purposely give themselves less than their medical dosage of insulin in order to control their weight gain. Lack of insulin creates a starvation state in the body, excreting sugars in urine instead of being stored in the body as fat or used for energy. Often, people with diabulimia had been diagnosed with another eating disorder prior to their diagnosis of Type 1 diabetes. There are severe health risks including diabetic neuropathy, renal failure, blindness, and death.

</p></div></div>

<div class="ExpandingDiv">

<h2>Dual Diagnosis </h2>
<a href="#" class="show_hide" rel="#slidingDiv5"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv5" style="display: none;"><p>
The term dual diagnosis describes the concurrent condition of a person suffering from a mental health disorder, like an eating disorder, and chemical dependency, or another mental health illness such as borderline personality disorder, bipolar disorder, depression, post traumatic stress, or chronic self injury.
</p></div></div>

<div class="ExpandingDiv">

<h2>Over-Exercising </h2>
<a href="#" class="show_hide" rel="#slidingDiv6"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv6" style="display: none;"><p>
There is no formula for determining how much too much exercise is. However, if exercise has become compulsive and an avenue for purging you could be over-exercising. Those suffering from Anorexia or Bulimia may engage in over-exercising rather than, or in addition to, use of laxatives, diuretics, or self-induced vomiting. This can become an addictive behavior when it puts the individuals' physical and emotional health, job, school, or family at risk.
</p></div></div>

<div class="ExpandingDiv">

<h2>EDNOS (Eating Disorder Not Otherwise Specified)</h2>
<a href="#" class="show_hide" rel="#slidingDiv7"><img src="../images/PlusSign.png" width="113" height="33" alt="Plus Sign"></a>

<div id="slidingDiv7" style="display: none;"><p>
This eating disorder classification is given to those who meet several requirements for anorexia, bulimia, or binge-eating disorder, but may lack one of the specifications or may have characteristics of several disorders. A diagnosis of EDNOS still requires treatment, as it stems from a psychological disorder often causing both anorexic and bulimic behaviors.
</p></div></div>


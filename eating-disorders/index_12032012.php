<h2>Anorexia Nervosa </h2>
<a name="anorexia"></a>
Anorexia nervosa is an eating disorder characterized by weight obsession, self-starvation, and distortion of body image. &nbsp;

<div id="slidingDiv" style="display: none;">People with this psychiatric disorder strive to maintain a weight far below normal for their height and age commonly, 
but not exclusively, by limiting food, excessively exercising, or fasting. For the anorexic, the pursuit of being thin has transformed into an obsession and a 
way to cope with other emotional problems to have a sense of control or self-worth.
</div>
<a href="#" class="show_hide" rel="#slidingDiv" id="anorexia_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="bulimia"></a>
<h2>Bulimia Nervosa</h2>
Bulimia nervosa is an eating disorder characterized by habitual stints of excessive food intake, known as "binge-eating," by a purging of calories through means
of self-induced comiting, laxative abuse, periods of fasting, or excessive exercise as methods of weight control. &nbsp;

<div id="slidingDiv2" style="display: none;">These episodes are typically done in secret and are associated with shame since much of the bulimic's self worth 
originates in body shape and size. Like the anorexic, these behaviors can become addictive as they strive for their "ideal" body proportions.
</div>
<a href="#" class="show_hide" rel="#slidingDiv2" id="bulimia_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="binge"></a>
<h2>Binge-Eating Disorder</h2>
Binge-eating disorder, like bulimia, is characterized by compulsively in taking large quantities of food at a single sitting, however, without purging away the 
calories afterwards. &nbsp;

<div id="slidingDiv3" style="display: none;">Over-eating becomes a habit and a coping mechanism for overcoming stress, depression, and other negative feelings. 
This emotional eating becomes an addiction and leads to very serious medical conditions linked with obesity such as high blood pressure and cholesterol levels.
</div>
<a href="#" class="show_hide" rel="#slidingDiv3" id="binge_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="diabulimia"></a>
<h2>Diabulimia </h2>
This eating disorder classification is characterized by people with Type 1 diabetes who purposely give themselves less than their medical dosage of insulin
in order to control their weight gain. &nbsp;

<div id="slidingDiv4" style="display: none;">Lack of insulin creates a starvation state in the body, excreting sugars in urine instead of being stored in the 
body as fat or used for energy. Often, people with diabulimia had been diagnosed with another eating disorder prior to their diagnosis of Type 1 diabetes. There 
are severe health risks including diabetic neuropathy, renal failure, blindness, and death.
</div>
<a href="#" class="show_hide" rel="#slidingDiv4" id="diabulima_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">

<h2>Co-occurring Disorder</h2>
The term co-occurring disorder describes the concurrent condition of a person suffering from a mental health disorder, like an eating disorder, &nbsp;

<div id="slidingDiv5" style="display: none;">and chemical dependency, or another mental health illness such as borderline personality disorder, bipolar disorder, 
depression, post traumatic stress, or chronic self injury.
</div>
<a href="#" class="show_hide" rel="#slidingDiv5">Learn More</a>
</div>


<div class="ExpandingDiv">

<h2>Over-Exercising </h2>
There is no formula for determining how much too much exercise is. However, if exercise has become compulsive and an avenue for purging, &nbsp;

<div id="slidingDiv6" style="display: none;">you could be over-exercising. Those suffering from Anorexia or Bulimia may engage in over-exercising rather than, 
or in addition to, use of laxatives, diuretics, or self-induced vomiting. This can become an addictive behavior when it puts the individuals' physical and 
emotional health, job, school, or family at risk.
</div>
<a href="#" class="show_hide" rel="#slidingDiv6">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="ednos"></a>
<h2>EDNOS (Eating Disorder Not Otherwise Specified)</h2>
This eating disorder classification is given to those who meet several requirements for anorexia, bulimia, or binge-eating disorder, &nbsp;

<div id="slidingDiv7" style="display: none;">but may lack one of the specifications or may have characteristics of several disorders. A diagnosis of EDNOS still 
requires treatment, as it stems from a psychological disorder often causing both anorexic and bulimic behaviors.
</div>
<a href="#" class="show_hide" rel="#slidingDiv7" id="ednos_BTN">Learn More</a>
</div>


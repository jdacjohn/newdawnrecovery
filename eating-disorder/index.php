<h2>Anorexia Nervosa </h2>
<a name="anorexia"></a>
Anorexia nervosa is an eating disorder characterized by weight obsession, self-starvation, and distortion of body image. &nbsp;

<div id="slidingDiv" style="display: none;"><br />People with this psychiatric disorder have an inability to maintain a body weight that is normal for their age and height. They often have severe body image disturbance where body image is the predominant measure of self-worth with a denial of the seriousness of their illness. It's not uncommon for individuals to use caloric restriction, excessive exercise and other compensatory behaviors to control emotional needs or pain.
</div>
<a href="#" class="show_hide" rel="#slidingDiv" id="anorexia_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="bulimia"></a>
<h2>Bulimia Nervosa</h2>
Bulimia nervosa is an eating disorder that consists of episodes of excessive food intake alternating with periods of purging and/or restricting. Patients who have bulimia may be of normal weight, or they may be under or overweight.   &nbsp;

<div id="slidingDiv2" style="display: none;"><br />
Individuals with this psychiatric disorder struggle with 'binge eating' which is characterized by eating an amount of food that is larger than what most people would eat in a discreet period of time. This is often accompanied by distress and a lack of control. The individual engages in compensatory behaviors that include self-induced vomiting; use of laxatives, diuretics or enemas,fasting; excessive exercise or strict diets. As with anorexia nervosa, body shape and weight are the predominant measures of self-worth. 
</div>
<a href="#" class="show_hide" rel="#slidingDiv2" id="bulimia_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="binge"></a>
<h2>Binge-Eating Disorder</h2>
Binge-eating disorder, like bulimia, is characterized by compulsively taking in large quantities of food at a single sitting &nbsp;

<div id="slidingDiv3" style="display: none;"><p>These individuals do not engage in compensatory behaviors such as vomiting, laxative abuse, excessive exercise or fasting.</p>

<p>It's not uncommon for over-eating to become habitual and to function as a coping mechanism for overcoming stress, depression, and other negative feelings. This emotional eating can lead to very serious medical conditions linked with obesity such as high blood pressure and cholesterol.
</p>

</div>
<a href="#" class="show_hide" rel="#slidingDiv3" id="binge_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="diabulimia"></a>
<h2>Diabulimia </h2>
This eating disorder subtype is characterized by people with Type 1 diabetes who purposely give themselves less than their medical dosage of insulin in order to control their weight gain. &nbsp;

<div id="slidingDiv4" style="display: none;"><br />Lack of insulin creates a starvation state in the body, excreting sugars in urine instead of being stored in the 
body as fat or used for energy. Often, people with diabulimia had been diagnosed with another eating disorder prior to their diagnosis of Type 1 diabetes. There 
are severe health risks including diabetic neuropathy, renal failure, blindness, and death.
</div>
<a href="#" class="show_hide" rel="#slidingDiv4" id="diabulimia_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">

<h2>Co-occurring Disorder</h2>
The term co-occurring disorder describes the diagnosis of more than one mental health or chemical dependency condition occurring at the same time. &nbsp;

<div id="slidingDiv5" style="display: none;"><br />This may include, but is not limited to, the following: eating disorders, depression, anxiety, bipolar disorder, post traumatic stress, chemical dependency, or another mental health conditions such as borderline personality or chronic self injury.
</div>
<a href="#" class="show_hide" rel="#slidingDiv5">Learn More</a>
</div>


<div class="ExpandingDiv">

<h2>Over-Exercising </h2>
Many individuals with anorexia and bulimia engage in over-exercising in addition to self-induced vomiting, caloric restriction, and/or the use of laxatives or diuretics. &nbsp;

<div id="slidingDiv6" style="display: none;"><br /> 
Exercise is often experienced as compulsive and takes precedence over relationships, family, school and work commitments. 
</div>
<a href="#" class="show_hide" rel="#slidingDiv6">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="ednos"></a>
<h2>EDNOS (Eating Disorder Not Otherwise Specified)</h2>
This eating disorder diagnosis comprises individuals who may have significant eating disorder symptoms but don't meet frequency or weight criteria. &nbsp;

<div id="slidingDiv7" style="display: none;"><br />
For those with anorexia, it may include patients who still have regular menses or patients who have lost a significant amount of weight but their weight is still in a 'normal' range. For those with bulimia, it included individuals who don't meet the frequency requirements for binges per week or the length of symptoms. This category also includes other maladaptive behaviors such as chewing and spitting, and it's currently used as the diagnostic category for binge eating disorder
</div>
<a href="#" class="show_hide" rel="#slidingDiv7" id="ednos_BTN">Learn More</a>
</div>


<h1>FAQs</h1>

<p><strong>Following participation in the intensive outpatient program, are follow-up services available at New Dawn Treatment Center?</strong></p>
<p>Yes, many graduates elect to continue participating in an outpatient group, individual nutritional counseling, and/or individual therapy to help with relapse prevention. In some cases, we will refer you to an outside specialist.</p>

<p><strong>Do I have to stop seeing my outpatient therapist if I begin the intensive outpatient program or day treatment with New Dawn?</strong></p>
<p>At New Dawn Treatment Center, we believe in coordinating care with your existing outpatient providers. As long as your outpatient therapist is supportive of your participation at New Dawn, you are welcome to continue seeing your therapist while participating in our program. A case manager will be assigned to work with you and your outpatient therapist to assist with regular coordination of your treatment plan</p>

<p><strong>How many weeks will I participate at New Dawn Treatment Center?</strong></p>
<p>The average length of stay is 12-15 weeks.</p>

<p><strong>What types of schedule options are available if I participate in New Dawn's intensive outpatient program?</strong></p>
<p>New Dawn's intensive outpatient program offers both morning and late afternoon/evening groups. At the time of your initial evaluation, we put together a treatment schedule that fits your needs. The majority of our patients start in the intensive outpatient program by attending 5 days per week and taper down in attendance as they progress in their recovery.</p>


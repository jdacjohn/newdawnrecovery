<h1>Fees</h1>

<h2>Self-Pay Options</h2>

<p>Unfortunately, residential treatment programs by and large are expensive, and can range anywhere from $10,000 to $70,000 per month. This might seem unreasonable to some, but when you take into account the cost of the personal, legal and financial problems that persist in the absence of successful treatment, the cost is minor in comparison. </p>
<p>For example, one DUI/DWI costs on average between $15,000 and $20,000, legal fees for divorce can easily cost $30,000 to $50,000, a liver transplant costs $300,000 on average, and treatment associated with a stroke costs about $150,000. Of course, the highest cost - that of losing the love and trust of family and friends - cannot be measured.
</p><p>
At New Dawn, we have a number of affordable treatment options available that are designed to make recovery accessible without causing financial strain. If you would like to pursue financing for your treatment, we have partnered with M-Lend, Clark Behavioral Health and Serenity Loans to offer you flexible financing options and low monthly payments. 
</p>

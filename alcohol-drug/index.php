  <h2>Alcohol</h2>
Alcoholism is the third most common mental illness and affects more than 14 million people in the United States. &nbsp;

<div id="slidingDiv" style="display: none;"><br />
Alcohol addiction differs from alcohol abuse in that one can become tolerant requiring more frequent and 
substantial amounts of alcohol in order to feel its effects, or experience withdrawal without use. Addiction can also be characterized by drinking more or 
for a longer period of time than planned and having trouble reducing your use. Possible signs of an alcohol problem include drinking alone to escape problems 
or for the purpose of getting drunk, hiding alcohol in unusual places, becoming aggravated when alcohol is not accessible, and drinking despite negative 
consequences. Attaining abstinence is possible, but without treatment results tend to be temporary.
</div>
<a href="#" class="show_hide" rel="#slidingDiv" id="alcohol_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="cocaine"></a>
<h2>Cocaine</h2>
Like alcohol dependence, cocaine dependence includes a tolerance build up to the drug withdrawal, using large quantities of the drug or for a longer period of time than intended, a strong craving to use, 
and unsuccessful attempts to stop using despite negative consequences.

<div id="slidingDiv2" style="display: none;"><br />
 Withdrawal symptoms can include suppressed appetite, insomnia, irritability and cocaine 
cravings. Cocaine addiction also increases the risk for suicide, violence and dangerous sexual interactions.  &nbsp;
</div>
<a href="#" class="show_hide" rel="#slidingDiv2" id="cocaine_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="marijuana"></a>
<h2>Marijuana</h2>
Many studies now show that marijuana dependency develops in approximately 4% of Americans and shares many symptoms of tobacco withdrawal including anger, irritability, decreased appetite, insomnia, and drug cravings. &nbsp;

<div id="slidingDiv4" style="display: none;"><br />
These symptoms tend to last between one to three weeks which can interfere with the attempt to quit using marijuana. Continued marijuana use develops increased risk for depression and abuse 
other drugs and alcohol.
</div>
<a href="#" class="show_hide" rel="#slidingDiv4" id="marijuana_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="heroin"></a>
<h2>Heroin</h2>
The physical addiction to heroin can occur within one to two weeks of daily use. &nbsp;

<div id="slidingDiv5" style="display: none;"><br />Like other drugs, a tolerance can be developed requiring higher doses to achieve a high. Often heroin use is 
concurrent with other opiates, particularly if heroin is not available. Physical withdrawal symptoms may include sweating, muscle pain, insomnia, anxiety, 
tremors and vomiting.
</div>
<a href="#" class="show_hide" rel="#slidingDiv5" id="heroin_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="methamphetamine"></a>
<h2>Methamphetamine</h2>
Methamphetamine creates a great concern due to its accessibility, varying chemical makeup and highly addictive qualities. &nbsp;

<div id="slidingDiv6" style="display: none;"><br />It is fairly typical for addiction to occur within one or two uses. Meth addiction has the tell-tale signs 
of significant weight loss, lack of routine (except to get high), rotting teeth, lack of personal hygiene, facial and body sores, and sleep deprivation.
</div>
<a href="#" class="show_hide" rel="#slidingDiv6" id="methamphetamine_BTN">Learn More</a>
</div>


<div class="ExpandingDiv">
<a name="scrips"></a>
<h2>Prescription Drugs</h2>
Addiction to prescription drugs occurs similar to illegal drug addiction, and can be avoided by following the directions and use for the prescription drugs. &nbsp;


<div id="slidingDiv7" style="display: none;"><br />Changing dosage of a drug without doctor consent and sharing prescriptions that can increase drug dependency.

<p>	Prescription drugs can be categorized into 5 main groups:</p>

<p><strong>Painkillers:</strong> Opioids such as codine and morphine. Other painkillers include oxycodone (OxyContin), hydrocodone (Vicodin, Norco), meperidine (Demerol), 
hydromorphone (Dilaudid), meperidine (Demerol), diphenoxylate (Lomotil) and proposyphene (Darvon).</p> 

<p><strong>CNS depressants/Tranquilizers</strong>: These benzodiazepine depressants are often used to treat anxiety disorders or insomnia and include alprazolam (Xanax), 
diazepam (Valium), and triazolam (Halcion).</p>

<p><strong>Barbiturates:</strong> Barbiturates are depressants prescribed to treat insomnia, tension, anxiety. Examples are phenobarbital (Luminal), pentobarbital (Nembutal), 
and mephobarbital (Mebaral).</p>

<p><strong>Stimulants:</strong> Stimulants are can be found as over the counter diet pills, or prescribed for ADHD or obesity. Methylphenidate (Ritalin, Concerta) and 
dextroamphetamine (Dexedrin, Adderall) fall under the category of stimulants.</p>

<p><strong>Dextromethorphan (DMX):</strong>Cough and cold medications use DMX as the active cough suppressant.</p>

</p></div>

<h1>Why New Dawn</h1>
<h2>Internationally Accredited</h2>

<p>Although accreditation with CARF (the Commission on Accreditation of Rehabilitation Facilities) is <strong>strictly voluntary</strong>, all of New Dawn’s programs are CARF accredited eating disorder and substance abuse treatment centers. CARF is an independent, nonprofit body whose international certification is the hallmark of quality because of their <strong>high treatment standards</strong> and requirement that accredited programs have a system in place for program improvement that relies at in part on gathering input from the persons served. Ours was the <strong>first program in the greater Sacramento area</strong> to be awarded this certification, a feat that demonstrates our commitment to providing the <strong>highest quality of treatment</strong> to our clients.</p>


